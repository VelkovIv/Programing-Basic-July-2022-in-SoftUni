# Напишете програма, която чете число n, въведено от потребителя, и чертае квадрат от n * n звездички.
# Разликата с предходната задача е, че между всеки две звездички има по един интервал.
n = int(input())

for i in range(n):
    row_print = ''
    for y in range(n):
        row_print += "* "
    print(row_print)